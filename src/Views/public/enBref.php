<section class="content-section alt" id="histoire">
  <div class="container">
    <div class="row g-5">
      <div class="col-lg-5">
        <div class="section-eyebrow">Notre parcours</div>
        <h2 class="section-title-bas">Notre histoire</h2>
        <p class="section-text">
          De « Pharmaciens Sans Frontières » à « Pharmacie Humanitaire Internationale », 40 ans de mobilisation au service des plus démunis.
        </p>
        <div class="photo-block mt-4">
            <img src='./Asset/Images/hisotire.PNG' width="100%">
        </div>
      </div>
      <div class="col-lg-7">
        <div class="timeline mt-lg-5">

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">1985</div>
            <div class="tl-text">Création du mouvement humanitaire des pharmaciens <strong>PSF « Pharmaciens Sans Frontières »</strong>. Des pharmaciens engagés commencent à agir en faveur de l'accès aux soins pour les plus démunis.</div>
          </div>

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">1995</div>
            <div class="tl-text">Développement du réseau d'antennes locales en France métropolitaine. L'association étend son maillage territorial pour toucher davantage de bénéficiaires.</div>
          </div>

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">2004</div>
            <div class="tl-text">PSF Union France devient <strong>P.H.I. « Pharmacie Humanitaire Internationale »</strong>, en poursuivant le même but : l'accès aux soins pour les plus démunis avec l'aide des pharmaciens.</div>
          </div>

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">2008 — 2009</div>
            <div class="tl-text">Création de l'<strong>Établissement pharmaceutique à Nîmes</strong>, agréé par l'ANSM. PHI devient la seule association habilitée à distribuer des médicaments neufs aux centres de soins caritatifs agréés ARS en France.</div>
          </div>

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">2022</div>
            <div class="tl-text">Mobilisation massive pour l'<strong>Ukraine</strong> dès les premiers jours du conflit. Plus de 35 semi-remorques de matériel médical acheminés vers les hôpitaux ukrainiens.</div>
          </div>

          <div class="tl-item">
            <div class="tl-dot"></div>
            <div class="tl-year">2025</div>
            <div class="tl-text"><strong>40 ans d'engagement</strong> — PHI fédère aujourd'hui 35 associations locales dans plus de 40 départements français et intervient dans plus de 20 pays à travers le monde.</div>
          </div>

        </div>
      </div>
    </div>
  </div>
