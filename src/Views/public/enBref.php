        </div>
      </div>
      <div class="col-lg-6">
        <div class="section-eyebrow">Le PDG</div>
        <h2 class="section-title">Le mot du président</h2>
        <p class="section-text">
          Depuis le 1er janvier 2009, PHI possède un <strong>établissement pharmaceutique à Nîmes</strong>, agréé par l'Agence Nationale de Sécurité du Médicament et des Produits de Santé (ANSM).
        </p>
        <p class="section-text">
          Géré par une pharmacienne responsable, cet établissement assure la distribution en gros de <strong>médicaments neufs</strong> — achetés directement auprès des laboratoires pharmaceutiques — à <strong>190 centres de soins caritatifs en France</strong> agréés par les ARS, dont 3 situés en Outre-mer.
        </p>
        <p class="section-text">
          Cette activité est <strong>financée intégralement par l'Assurance Maladie</strong> et permet un approvisionnement sécurisé, pérenne et gratuit des structures de soins pour les personnes en grande précarité.
        </p>
      </div>
    </div>
  </div>
</section>
