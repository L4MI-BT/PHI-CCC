        <h2 class="section-title">40 ans d'engagement pour l'accès aux soins</h2>
        <p class="section-text">
        Tout commence souvent par un geste simple.
        Un carton ouvert, des mains qui trient, un regard attentif posé sur un médicament, un dispositif médical préparé avec soin.
        </p>
        <p class="section-text">
         Depuis près de quarante ans, Pharmacie Humanitaire Internationale (PHI) est née de cette conviction l’accès aux soins ne devrait jamais dépendre du lieu où l’on vit, ni de sa situation. 
        </p>
        <p class="section-text">
         En France comme à l’international, PHI agit là où les besoins sont réels, parfois discrets, souvent urgents. Derrière chaque action, il y a des bénévoles engagés, des professionnels de santé, des associations locales, qui coopèrent pour répondre avec justesse aux réalités du terrain.

        </p>
        <p class="section-text">
            PHI ne se contente pas d’envoyer du matériel. Elle accompagne, sécurise, forme et construit. Elle soutient les structures de soins existantes, renforce les équipes locales et agit dans le respect de celles et ceux qui soignent au quotidien.
        </p>
